    /**
     * Atualiza a lista de jogadores conectados na tela
     * @param jogadores - Lista de nomes dos jogadores
     */
    public void atualizarTela(List<String> jogadores) {
        StringBuilder texto = new StringBuilder();
        texto.append("=== Jogadores Conectados ===\n\n");

        for (int i = 0; i < jogadores.size(); i++) {
            texto.append((i + 1)).append(". ").append(jogadores.get(i)).append("\n");
        }

        texto.append("\nTotal: ").append(jogadores.size()).append(" jogador(es)");

        // Atualiza a interface na Event Dispatch Thread
        final String textoFinal = texto.toString();
        javax.swing.SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                jTextArea1.setText(textoFinal);
            }
        });
    }

